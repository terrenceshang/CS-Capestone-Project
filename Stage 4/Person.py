class Person:
    def __init__(self, username, name, surname, email, password):
        self.username = username
        self.name = name
        self.surname = surname
        self.email = email
        self.password = password

    def getName():
        return self.name
    def getUsername():
        return self.username
    def getSurname():
        return self.surname
    def getEmail():
        return self.email
    def getPassword():
        return self.password
